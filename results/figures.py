import numpy as np
import matplotlib.pyplot as plt

import sys
sys.path.append('.')

# Load the numpy files
AGM_training_loss = np.load('./train/AGM_train_loss.npy')
AGM_testing_loss = np.load('./val/AGM_val_loss.npy')
AGM_training_acc = np.load('./train/AGM_train_acc.npy')
AGM_testing_acc = np.load('./val/AGM_val_acc.npy')

SGD_training_loss = np.load('./train/SGD_train_loss.npy')
SGD_testing_loss = np.load('./val/SGD_val_loss.npy')
SGD_training_acc = np.load('./train/SGD_train_acc.npy')
SGD_testing_acc = np.load('./val/SGD_val_acc.npy')

Adam_training_loss = np.load('./train/Adam_train_loss.npy')
Adam_testing_loss = np.load('./val/Adam_val_loss.npy')
Adam_training_acc = np.load('./train/Adam_train_acc.npy')
Adam_testing_acc = np.load('./val/Adam_val_acc.npy')

Lion_training_loss = np.load('./train/Lion_train_loss.npy')
Lion_testing_loss = np.load('./val/Lion_val_loss.npy')
Lion_training_acc = np.load('./train/Lion_train_acc.npy')
Lion_testing_acc = np.load('./val/Lion_val_acc.npy')

# print(val_acc)

plt.figure(figsize=(10, 5))
plt.plot(AGM_training_loss, label='AGM')
plt.plot(SGD_training_loss, label='SGD')
plt.plot(Adam_training_loss, label='Adam')
plt.plot(Lion_training_loss, label='Lion')
plt.xlabel('Epoch')
plt.ylabel('Loss')
plt.title('Training Loss per Epoch')
plt.legend()
plt.grid(True)
plt.savefig('Training_loss_per_epoch.png')
plt.show()

plt.figure(figsize=(10, 5))
plt.plot(AGM_testing_loss, label='AGM')
plt.plot(SGD_testing_loss, label='SGD')
plt.plot(Adam_testing_loss, label='Adam')
plt.plot(Lion_testing_loss, label='Lion')
plt.xlabel('Epoch')
